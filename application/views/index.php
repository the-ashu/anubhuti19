<section id="about" class="section-padding">
      <div class="container">
        <div class="row">
          <div class="col-12">
            <div class="section-title-header text-center">
              <h1 class="section-title wow fadeInUp" data-wow-delay="0.2s">About Us!</h1>
              <p class="wow fadeInDown" data-wow-delay="0.2s"></p>
            </div>
          </div>
        </div>
<p>
In Any College There is Need of the books when one enters to the new session, and hence need books for the same. For this purpose one usually prefers to get the book from someone temporarily and make it returned at the end of session to the one from whom the book has been borrowed.
 So in doing so one is usually in a trouble to find such guys whose books/stationary can be used for same.But it is not easy to get these many peoples  and hence in case of nothing one has no option rather than taking a new book.
So to make this procedure in a smooth way <bold>Book Pool  </bold> can play a useful role in providing books to the needy one.
A <bold>Book Pool </bold> is for the same task which is done manually by the students by wandering in search for books in the college. We Took the books from the people who desire to give their books when not in  their use and give it to the students who need them. We Put all these books in our store whose availability is tracked by the Book Pool System.

 The system helps both students and  manager to keep a constant track of all the books available in the Store. 
 It allows both the admin and the student to search for the desired book.
 It becomes necessary for the manager  to keep a continuous check on the books issued and returned.
 This task if carried out manually will be tedious and includes chances of mistakes.
 These errors are avoided by allowing the system to keep track of information such as issue date, last date to return the book and even fine information and thus there is no need to keep manual track of this information which thereby avoids chances of mistakes.
Thus this system reduces manual work to a great extent allows smooth flow of library activities by removing chances of errors in the details
<br><i> In reward we give points to the Particular students who gives the books for the same and that point can be used by the studnets to take the  particular books </i></p>
</div>
</section>

